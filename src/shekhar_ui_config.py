def get_component_style(name):
    return ui_component_registry.get(name, '')

def build_inline_style(*names):
    return ' '.join(get_component_style(n) for n in names)

def list_components():
    return list(ui_component_registry.keys())

def component_count():
    return len(ui_component_registry)

def search_components(keyword):
    return {k: v for k, v in ui_component_registry.items() if keyword.lower() in k.lower()}

def export_css_sheet(filepath='ui_styles.css'):
    with open(filepath, 'w') as f:
        for name, style in ui_component_registry.items():
            f.write(f'.{name} {{ {style} }}\n')
    return filepath

def get_dark_components():
    return {k: v for k, v in ui_component_registry.items() if 'dark' in k or 'glass' in k or 'neon' in k}

def get_animation_components():
    return {k: v for k, v in ui_component_registry.items() if 'animation' in v or 'transition' in v}
